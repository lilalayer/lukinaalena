print("Введите значение координатов x1 y1 через пробел")
x1, y1 = map(int, input().split())
print("Введите значение координатов x2 y2 через пробел")
x2, y2 = map(int, input().split())
bc=((y2-y1)**2)**0.5
ab=((x2-x1)**2)**0.5
p=2*(ab+bc)
s=ab*bc
print("Периметр равен", p, "Площадь равна", s)
